# ── Helpers ──────────────────────────────────────────────────────────────────
RESET  = "\033[0m"
RED    = "\033[31m"
GREEN  = "\033[32m"
YELLOW = "\033[33m"
CYAN   = "\033[36m"

def eos_size(eos_path):
    """Return file size in bytes via xrdfs stat, or -1 on error."""
    r = subprocess.run(
        ["xrdfs", "root://eosuser.cern.ch", "stat", eos_path],
        capture_output=True, text=True, timeout=20,
    )
    for line in r.stdout.splitlines():
        if "Size:" in line:
            try:
                return int(line.split("Size:")[1].strip().split()[0])
            except ValueError:
                pass
    return -1

def check_file(ds, pid):
    """Return (ok, reason, entries, size_bytes)."""
    eos_path = f"{EOS_BASE}/{ds}/omtf_hits_{ds}_{pid}.root"
    url       = f"{XROOTD_PFX}/{eos_path}"

    size = eos_size(eos_path)
    if size < 0:
        return False, "not found / stat failed", 0, size
    if size < 5_000:
        return False, f"file too small ({size} bytes)", 0, size

    tf = ROOT.TFile.Open(url)
    if not tf or tf.IsZombie():
        if tf:
            tf.Close()
        return False, "zombie / cannot open", 0, size

    tree = tf.Get(TREE_PATH)
    if not tree:
        tf.Close()
        return False, f"TTree '{TREE_PATH}' not found", 0, size

    entries = tree.GetEntries()
    _, _, min_entries = SIGNAL_DATASETS[ds]
    tf.Close()

    if entries < min_entries:
        return False, f"too few entries: {entries} < {min_entries}", entries, size

    return True, "OK", entries, size

def eos_rm(eos_path):
    r = subprocess.run(
        ["xrdfs", "root://eosuser.cern.ch", "rm", eos_path],
        capture_output=True, text=True, timeout=20,
    )
    return r.returncode == 0

# ── Main ───────────────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--delete",   action="store_true", help="Delete bad EOS files")
    parser.add_argument("--resubmit", action="store_true", help="Write condor resubmit file")
    args = parser.parse_args()

    bad_files  = []   # (ds, pid, reason, entries, size)
    total_ok   = 0
    total_bad  = 0
    total_miss = 0    # expected but not in this production run yet

    # Track which original (50k) ProcIds are expected in full_production.sub
    FULL_PROD_PIDS = {
        "S1": set(range(10)),
        "S2": set(range(10)),
        "S3": set(range(10)),
        "S4": set(range(3)),
        "S5": set(range(3)),
    }

    for ds, (pids, min_entries, nevents_orig) in SIGNAL_DATASETS.items():
        print(f"\n{CYAN}{'─'*60}{RESET}")
        print(f"{CYAN}  Dataset: {ds}  ({len(pids)} expected files){RESET}")
        print(f"{CYAN}{'─'*60}{RESET}")
        for pid in pids:
            ok, reason, entries, size = check_file(ds, pid)
            size_mb = size / 1e6 if size > 0 else 0
            if ok:
                total_ok += 1
                print(f"  {GREEN}OK{RESET}  {ds}_{pid}  entries={entries:,}  size={size_mb:.2f} MB")
            else:
                total_bad += 1
                print(f"  {RED}BAD{RESET} {ds}_{pid}  {reason}  size={size_mb:.2f} MB")
                bad_files.append((ds, pid, reason, entries, size, nevents_orig))
                if args.delete and size > 0:
                    eos_path = f"{EOS_BASE}/{ds}/omtf_hits_{ds}_{pid}.root"
                    removed = eos_rm(eos_path)
                    print(f"       → {'Deleted' if removed else 'DELETE FAILED'}: {eos_path}")

    # Summary
    total = total_ok + total_bad
    print(f"\n{'='*60}")
    print(f"  Total checked : {total}")
    print(f"  {GREEN}OK{RESET}            : {total_ok}")
    print(f"  {RED}BAD{RESET}           : {total_bad}")
    print(f"{'='*60}")

    if bad_files:
        print(f"\n{YELLOW}Bad files summary:{RESET}")
        for ds, pid, reason, entries, size, _ in bad_files:
            print(f"  {ds}_{pid:3d}  reason='{reason}'  entries={entries}  size={size}")

        if args.resubmit:
            sub_path = os.path.join(
                os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
                "condor", "resubmit_signal_bad.sub"
            )
            with open(sub_path, "w") as f:
                f.write(CONDOR_HEADER)
                for ds, pid, _, _, _, nevents_orig in bad_files:
                    # Split each bad 50k job into 100 × 500-event jobs
                    # Use ProcIds starting at 1000*pid to avoid clashing
                    base_procid = pid * 100 + 1000  # safe non-overlapping range
                    for i in range(100):
                        new_pid  = base_procid + i
                        skip     = i * 500
                        f.write(f"{ds} {new_pid} {skip} 500\n")
                f.write(")\n")
            njobs = sum(100 for _ in bad_files)
            print(f"\n{YELLOW}Wrote {njobs} jobs → {sub_path}{RESET}")
            print(f"Submit with:  condor_submit condor/resubmit_signal_bad.sub")
    else:
        print(f"\n{GREEN}All signal files passed integrity checks.{RESET}")

if __name__ == "__main__":
    main()
